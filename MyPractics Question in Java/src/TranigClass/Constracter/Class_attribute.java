package TranigClass.Constracter;

public class Class_attribute {
    int x;// Create a class attribute

    // Create a class constructor for Class_attribute
    public Class_attribute(){
        x = 5; //set the initial value for the class attribute x

    }

    public static void main(String[] args) {
        Class_attribute myObj = new Class_attribute(); //create an on object class for Class_attribute

        System.out.println(myObj.x);







    }

}
